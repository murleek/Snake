namespace Snake.Properties
{
    class Circle
    {
        public int X { get; set; } // X pos
        public int Y { get; set; } // Y pos
        public Circle()
        {
            X = 0;
            Y = 0;
        }
    }

    class Square
    {
        public int X { get; set; }
        public int Y { get; set; }

        public Square()
        {
            X = 1;
            Y = 1;
        }
    }
}
